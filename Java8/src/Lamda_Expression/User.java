package Lamda_Expression;

public class User {
	String name;
	int age;
	double sal;

	public User(String name, int age, double sal) {
		super();
		this.name = name;
		this.age = age;
		this.sal = sal;
	}

	@Override
	public String toString() {
		return "User [name=" + name + ", age=" + age + ", sal=" + sal + "]";
	}
		
	

	}


